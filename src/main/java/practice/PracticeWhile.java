package practice;

public class PracticeWhile {

	public static void practiceWhile() {

		int num = 100;

		while (num >= 0) {
			System.out.println(num);

			num = num - 1;
		}

	}

	public static void practiceWhile2() {

		int num = 101;

		while (num > 0) {
			num = num - 1;

			System.out.println(num);
		}

	}

}